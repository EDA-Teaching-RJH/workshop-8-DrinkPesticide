# basic calculator functions library,
# if there was a main() function for running the calculator 
# if __name__ == "__main__": is used to allow for testing functionality
def addition(a, b):
    c = (a+b)
    return c 

def subtraction(a, b):
    c = (a-b)
    return c 

def multiplication(a, b):
    c = (a*b)
    return c   

def division(a, b):
    c = (a/b)
    return c

